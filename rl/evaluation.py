"""Evaluation helpers for reinforcement-learning trading agents."""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any

import pandas as pd

from evaluation.metrics import max_drawdown, sharpe_ratio
from visualization.research_charts import prepare_equity_curve


@dataclass(frozen=True)
class RLStrategyEvaluation:
    """Trading metrics for an RL policy rollout."""

    total_return: float
    sharpe: float
    max_drawdown: float
    observations: int


def evaluate_strategy_returns(
    returns: pd.Series,
    *,
    periods_per_year: int = 252,
) -> RLStrategyEvaluation:
    """Evaluate strategy returns generated by an RL environment or policy."""

    clean = returns.dropna()
    if clean.empty:
        raise ValueError("returns cannot be empty")

    equity_curve = prepare_equity_curve(clean)
    return RLStrategyEvaluation(
        total_return=float(equity_curve.iloc[-1] - 1.0),
        sharpe=sharpe_ratio(clean, periods_per_year=periods_per_year),
        max_drawdown=max_drawdown(equity_curve),
        observations=len(clean),
    )


def rollout_policy(env: Any, policy: Any, *, deterministic: bool = True) -> pd.Series:
    """Run a fitted policy through an environment and return strategy returns."""

    observation, _ = env.reset()
    terminated = False
    truncated = False

    while not (terminated or truncated):
        action, _ = policy.predict(observation, deterministic=deterministic)
        observation, _, terminated, truncated, _ = env.step(int(action))

    return env.strategy_returns()


def compare_rl_to_signal_strategy(
    rl_returns: pd.Series,
    signal_returns: pd.Series,
) -> pd.DataFrame:
    """Compare RL strategy performance against a model-driven signal strategy."""

    rl_eval = evaluate_strategy_returns(rl_returns)
    signal_eval = evaluate_strategy_returns(signal_returns)
    return pd.DataFrame(
        {
            "rl_strategy": rl_eval.__dict__,
            "signal_strategy": signal_eval.__dict__,
        }
    ).T
